package SwitchCase;

public class SwitchCase {
	public static void main(String args[]) {
		
		int value = 8;
		switch (value) {
		
		case 2:
			System.out.println("Congratulations here is the case 2 executed");
			break;
		case 8:
			System.out.println("Congratulations here is the case 8 executed");
			break;
		case 5:
			System.out.println("Congratulations here is the case 5 executed");
			break;
		default :
			System.out.println("Sorry none of your cases matched");
		}
	}

}
