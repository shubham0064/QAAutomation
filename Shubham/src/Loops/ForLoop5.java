package Loops;

public class ForLoop5 {

	public static void main(String[] args) {
		
		for(int i=0; i<50;i++) {
			System.out.println(i);
			i=i+4;
		}
		
		System.out.println("_______EVEN NO________");
		for(int i=0;i<10;i++) {
			System.out.println(i);
			i=i+1;
		}
		
		System.out.println("_________ODD NO_______");
		for(int i=1;i<10;i++) {
			System.out.println(i);
			i=i+1;
		}

	}

}
