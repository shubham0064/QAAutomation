package Loops;

public class Forloop4 {

	public static void main(String[] args) {
		
		for(int i = 0;i < 2;i++) {
			System.out.println("___________Hello: "+i);
			
		   for(int j = 0; j < 2;j++) {
			   System.out.println("******Hi: "+j);
			   
			   for(int k = 0;k < 2;k++) {
				   System.out.println("Bye: "+k);
			   }
		   }
			   
		}

}
}