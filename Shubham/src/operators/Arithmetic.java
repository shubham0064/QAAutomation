package operators;

public class Arithmetic {
	
	public static void main(String[] args) {
		
		int number1 = 13, number2 = 4, res = number1 + number2;
		System.out.println("Sum is: "+res);
		
		res = number1 - number2;
		System.out.println("Difference is: "+res);
		
		res = number1 * number2;
		System.out.println("Multiplied value is: "+res);
		
		res = number1 / number2;
		System.out.println("Quotient is: "+res);//Division operation
		
		res = number1 % number2;
		System.out.println("Remainder is: "+res);//Modulus operation
	}
	
}
	
	

