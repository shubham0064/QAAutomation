package operators;

public class Logical {

	public static void main(String[] args) {
	
		boolean bool1 = true, bool2 = true;
		System.out.println("bool1 && bool2: "+(bool1 && bool2));//true
		
		
		System.out.println("bool1 || bool2: "+(bool1 || bool2));//true
		
		
		System.out.println("(!bool1 && bool2): "+!(bool1 && bool2));//false
		
		
		
		


	}

}
